#ifndef C_LIBRARY_CREATION_AND_USAGE_OP_H
#define C_LIBRARY_CREATION_AND_USAGE_OP_H
/**
 * This method calculates the sum of the two given arguments
 * @param a
 * @param b
 * @return the sum of the two given arguments
 */

 int sum(int a, int b)
{
     double result = a + b;
     return result;
}

/**
 * This method calculates the difference of the two given arguments
 * @param a
 * @param b
 * @return the difference of the two given arguments
 */

int difference(int a, int b)
{
    double result = a - b;
    return result;
}
#endif //C_LIBRARY_CREATION_AND_USAGE_OP_H
